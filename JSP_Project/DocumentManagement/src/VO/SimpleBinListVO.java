package VO;

public class SimpleBinListVO {
int docId;
String title;
String type;
String ctime;
String sender;
public int getDocId() {
	return docId;
}
public void setDocId(int docId) {
	this.docId = docId;
}
public String getTitle() {
	return title;
}
public void setTitle(String title) {
	this.title = title;
}
public String getType() {
	return type;
}
public void setType(String type) {
	this.type = type;
}
public String getCtime() {
	return ctime;
}
public void setCtime(String ctime) {
	this.ctime = ctime;
}
public String getSender() {
	return sender;
}
public void setSender(String sender) {
	this.sender = sender;
}
}
